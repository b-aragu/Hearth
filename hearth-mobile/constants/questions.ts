/**
 * Get the daily question based on days together
 * Day 1 = Question 0, Day 100 = Question 99
 * After Day 100, it cycles back
 */
export const getQuestionForDay = (daysTogether: number): string => {
    const index = ((daysTogether - 1) % DAILY_QUESTIONS.length);
    return DAILY_QUESTIONS[Math.max(0, index)];
};

/**
 * Get the question index (1-100) for display
 */
export const getQuestionNumber = (daysTogether: number): number => {
    return ((daysTogether - 1) % DAILY_QUESTIONS.length) + 1;
};

